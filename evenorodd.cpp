#include<iostream>
using namespace std;
int main()
{
    cout<<"Even or Odd program"<<endl;
    cout<<"Enter number"<<endl;
    int n;
    cin>>n;

    if(n%2!=0)
    {
        cout<<"The number entered "<<n<<" is odd"<<endl;

    } 
    else
    {
        cout<<"The number entered "<<n<<" is even"<<endl;
    }
return 0;




}